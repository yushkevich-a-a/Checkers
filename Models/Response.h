#pragma once

enum class Response
{
    OK, // пользователь сделал ход 
    BACK, // значение возвращаемое по нажатию кнопки шаг назад
    REPLAY, // значение возвращаемое по нажатию кнопки запустить игру заново
    QUIT, // значение возвращаемое выключения игры
    CELL // значение возвращаемое при выборе пользователем клетки
};
